import UIKit

extension UIColor {

  @nonobjc class var mainColor: UIColor {
    return UIColor(red: 176.0 / 255.0, green: 24.0 / 255.0, blue: 45.0 / 255.0 , alpha: 1.0)
  }
    
    @nonobjc class var greyColor: UIColor {
        return UIColor(red: 126.0 / 255.0, green: 118.0 / 255.0, blue: 118.0 / 255.0 , alpha: 0.8)
    }

    
  
}
